// icon returns an inline SVG for the named glyph (empty string if unknown). The icon inherits
// the current text color and is sized in em units by CSS.
func icon(name string) string {
	p, ok := iconPaths[name]
	if !ok {
		return ""
	}
	return `<svg class="icon icon-` + name + `" viewBox="0 0 24 24" fill="none" stroke="currentColor" ` +
		`stroke-width="2" stroke-linecap="round" stroke-linejoin="round" aria-hidden="true">` + p + `</svg>`
}
